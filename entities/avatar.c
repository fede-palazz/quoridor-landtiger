static uint8_t chosenAvatars = 0;
int randomIndex;

/* Function that returns a random avatar */
Avatar get_random_avatar(Color color) {
  Avatar avatar;
	srand(time(NULL));
	
	// Check whether the avatar was already assigned
	do {
		randomIndex = rand() % NUM_AVATARS;
	}
	while (chosenAvatars & (1 << randomIndex));
	
	// Set current avatar as taken
	chosenAvatars |= (1 << randomIndex);
	switch (randomIndex) {
		case 0:
      avatar.pixelData = avatar_1_pixel_data;
			break;
    case 1:
      avatar.pixelData = avatar_2_pixel_data;
			break;
    default:
			break;
    }
	avatar.color = color;
	return avatar;
}
